package com.technobrain.trips.reference.model;

import com.technobrain.trips.core.model.BaseRefModelObject;

import java.io.Serializable;

import java.sql.Timestamp;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

@Entity
@NamedQueries({
@NamedQuery(
    name = "RefApplicationType.findAllApplicationTypeByIndicator", 
    query = "select new com.technobrain.trips.dto.CodeAndDescription(o.code,o.description) from RefApplicationType o where o.appIndicator = ?1"),
@NamedQuery(name = "RefApplicationType.findAll", query = "select o from RefApplicationType o")
})
@Table(name = "REF_APPLICATION_TYPE")
public class RefApplicationType extends BaseRefModelObject {
    @Id
    @Column(nullable = false)
    private String code;
    private String description;
    @Column(name="EFFECTIVE_DATE")
    private Timestamp effectiveDate;
    @Column(name="EXPIRY_DATE")
    private Timestamp expiryDate;
    @Column(name="APP_INDICATOR")
    private String appIndicator;

    public RefApplicationType() {
    }


    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Timestamp getEffectiveDate() {
        return effectiveDate;
    }

    public void setEffectiveDate(Timestamp effectiveDate) {
        this.effectiveDate = effectiveDate;
    }

    public Timestamp getExpiryDate() {
        return expiryDate;
    }

    public void setExpiryDate(Timestamp expiryDate) {
        this.expiryDate = expiryDate;
    }

    public void setAppIndicator(String appIndicator) {
        this.appIndicator = appIndicator;
    }

    public String getAppIndicator() {
        return appIndicator;
    }
}
